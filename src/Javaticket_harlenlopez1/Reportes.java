    private void IngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_IngresarActionPerformed
        // TODO add your handling code here:
        montoTotal=0;
        fechaInicial=jDateChooser1.getDate();
        fechaFinal=jDateChooser2.getDate();
        if(fechaInicial!=null||fechaFinal!=null){
            if(fechaInicial.before(fechaFinal)){
                modelo.clear();
                llamar.ingresoGeneradoPorFecha(0);
                modelo.addElement("Monto Total de Ingreso Generado por Fecha: "+montoTotal);
                jList1.setModel(modelo);
                jDateChooser1.setVisible(false);
                jDateChooser2.setVisible(false);
                fechainicial.setVisible(false);
                fechafinal.setVisible(false);
                Ingresar.setVisible(false);
            }else{
                JOptionPane.showMessageDialog(null, "Asegurese de poner las fechas correctamente");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Asegurese de llenar todas las fechas, porfavor.");
        }
    }//GEN-LAST:event_IngresarActionPerformed

